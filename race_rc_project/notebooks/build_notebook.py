    "# ── 3.1 Text cleaning ──\n",
    "import string\n",
    "\n",
    "def clean_text(text):\n",
    "    \"\"\"Lowercase, remove punctuation, strip extra whitespace.\"\"\"\n",
    "    text = str(text).lower()\n",
    "    text = text.translate(str.maketrans('', '', string.punctuation))\n",
    "    text = re.sub(r'\\s+', ' ', text).strip()\n",
    "    return text\n",
    "\n",
    "# Apply to all text columns\n",
    "for df in [train_df, val_df, test_df]:\n",
    "    df['article_clean']  = df['article'].apply(clean_text)\n",
    "    df['question_clean'] = df['question'].apply(clean_text)\n",
    "    for opt in ['A','B','C','D']:\n",
    "        df[f'{opt}_clean'] = df[opt].apply(clean_text)\n",
    "\n",
    "print('Text cleaning done.')\n",

    "%%writefile ../src/inference.py\n",
    "\"\"\"inference.py — Unified inference API.\"\"\"\n",
    "import os, re, string, time, warnings\n",
    "import numpy as np\n",
    "import joblib\n",
    "warnings.filterwarnings('ignore')\n",
    "\n",
    "from scipy.sparse import hstack, csr_matrix\n",
    "from sklearn.metrics.pairwise import cosine_similarity\n",
    "from collections import Counter\n",
    "\n",
    "_MODELS = {}\n",
    "\n",
    "BASE_DIR = os.path.dirname(os.path.abspath(__file__))\n",
    "MODELS_A = os.path.join(BASE_DIR, '..', 'models', 'model_a', 'traditional')\n",
    "MODELS_B = os.path.join(BASE_DIR, '..', 'models', 'model_b', 'traditional')\n",
    "\n",
    "\n",
    "def _load_models():\n",
    "    if _MODELS: return\n",
    "    _MODELS['ohe']      = joblib.load(os.path.join(MODELS_A, 'ohe_vectorizer.pkl'))\n",
    "    _MODELS['lr']       = joblib.load(os.path.join(MODELS_A, 'lr_model.pkl'))\n",
    "    _MODELS['svm']      = joblib.load(os.path.join(MODELS_A, 'svm_model.pkl'))\n",
    "    _MODELS['rf']       = joblib.load(os.path.join(MODELS_A, 'rf_model.pkl'))\n",
    "    _MODELS['dist_vec'] = joblib.load(os.path.join(MODELS_B, 'vectorizer_b.pkl'))\n",
    "    _MODELS['dist_rk']  = joblib.load(os.path.join(MODELS_B, 'distractor_ranker.pkl'))\n",
    "    _MODELS['hint_sk']  = joblib.load(os.path.join(MODELS_B, 'hint_scorer.pkl'))\n",
    "\n",
    "\n",
    "def _clean(text):\n",
    "    text = str(text).lower()\n",
    "    text = text.translate(str.maketrans('', '', string.punctuation))\n",
    "    return re.sub(r'\\s+', ' ', text).strip()\n",
    "\n",
    "\n",
    "def predict_answer(article, question, options):\n",
    "    \"\"\"Returns predicted answer letter ('A','B','C','D').\"\"\"\n",
    "    _load_models()\n",
    "    ohe = _MODELS['ohe']; lr = _MODELS['lr']; svm = _MODELS['svm']; rf = _MODELS['rf']\n",
    "    arts = _clean(article); qs = _clean(question)\n",
    "    texts, lex_rows = [], []\n",
    "    for opt in options:\n",
    "        os_c = _clean(opt)\n",
    "        texts.append(f'{arts} [SEP] {qs} [SEP] {os_c}')\n",
    "        at = set(arts.split()); qt = set(qs.split()); ot = set(os_c.split())\n",
    "        ol = os_c.split()\n",
    "        pos = arts.find(ol[0]) / max(len(arts),1) if ol and ol[0] in arts else 0.0\n",
    "        lex_rows.append([len(ol), len(qs.split()), len(qt&ot), len(ot&at), pos])\n",
    "    X_ohe = ohe.transform(texts)\n",
    "    art_vec = type(ohe)(binary=True, vocabulary=ohe.vocabulary_)\n",
    "    art_m = art_vec.transform([arts]*4)\n",
    "    opt_m = art_vec.transform([_clean(o) for o in options])\n",
    "    cos_f = csr_matrix(np.array([cosine_similarity(art_m[i], opt_m[i])[0,0] for i in range(4)]).reshape(-1,1))\n",
    "    lex_f = csr_matrix(np.array(lex_rows, dtype=np.float32))\n",
    "    X = hstack([X_ohe, cos_f, lex_f])\n",
    "    lr_p  = lr.predict_proba(X)[:,1]\n",
    "    svm_p = svm.predict_proba(X)[:,1]\n",
    "    rf_p  = rf.predict_proba(lex_f.toarray())[:,1]\n",
    "    scores = (lr_p + svm_p + rf_p) / 3\n",
    "    return ['A','B','C','D'][int(np.argmax(scores))]\n",
    "\n",
    "\n",
    "def generate_distractors(article, question, answer, n=3):\n",
    "    \"\"\"Returns list of 3 distractor strings.\"\"\"\n",
    "    _load_models()\n",
    "    dvec = _MODELS['dist_vec']; drk = _MODELS['dist_rk']\n",
    "    tokens = _clean(article).split()\n",
    "    ans_c = _clean(answer)\n",
    "    cands = list({' '.join(tokens[i:i+n_]) for n_ in range(1,3) for i in range(len(tokens)-n_+1)\n",
    "                  if ' '.join(tokens[i:i+n_]) != ans_c and len(' '.join(tokens[i:i+n_])) > 2})\n",
    "    if len(cands) < n:\n",
    "        freq = Counter(tokens)\n",
    "        stop = {'the','a','an','is','was','are','were','of','in','to','and','or','it'}\n",
    "        cands += [t for t,_ in freq.most_common(50) if t not in _clean(answer).split() and t not in stop][:n]\n",
    "    feats = []\n",
    "    for cand in cands[:80]:\n",
    "        cv = dvec.transform([cand]); av = dvec.transform([ans_c])\n",
    "        arv = dvec.transform([_clean(article)])\n",
    "        cos_a = cosine_similarity(cv, av)[0,0]\n",
    "        cos_r = cosine_similarity(cv, arv)[0,0]\n",
    "        freq_f = tokens.count(cand.split()[0]) / max(len(tokens),1)\n",
    "        char_f = sum(1 for a,b in zip(cand, ans_c) if a==b) / max(len(ans_c),1)\n",
    "        lr_f = len(cand.split()) / max(len(ans_c.split()),1)\n",
    "        feats.append([cos_a, cos_r, freq_f, char_f, lr_f])\n",
    "    scores = drk.predict_proba(feats)[:,1]\n",
    "    ranked = [cands[i] for i in np.argsort(scores)[::-1]]\n",
    "    selected = []\n",
    "    for cand in ranked:\n",
    "        if len(selected) == n: break\n",
    "        if not selected:\n",
    "            selected.append(cand)\n",
    "        else:\n",
    "            cv = dvec.transform([cand])\n",
    "            if not any(cosine_similarity(cv, dvec.transform([s]))[0,0] > 0.8 for s in selected):\n",
    "                selected.append(cand)\n",
    "    while len(selected) < n:\n",
    "        selected.append(ranked[len(selected)] if len(ranked) > len(selected) else 'N/A')\n",
    "    return selected[:n]\n",
    "\n",

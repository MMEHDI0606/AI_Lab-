cells.append(code([
    "# ── 3.4 Cosine similarity feature (article vs option) ──\n",
    "from sklearn.metrics.pairwise import cosine_similarity as cos_sim\n",
    "from scipy.sparse import csr_matrix\n",
    "\n",
    "print('Computing cosine similarity features...')\n",
    "art_vec = CountVectorizer(binary=True, max_features=5000, vocabulary=ohe_vec.vocabulary_)\n",
    "\n",
    "def cos_feature(df_exp):\n",
    "    art_mat = art_vec.transform(df_exp['article'])\n",
    "    opt_mat = art_vec.transform(df_exp['option'])\n",
    "    # Row-wise cosine similarity\n",
    "    scores = np.array([\n",
    "        cos_sim(art_mat[i], opt_mat[i])[0,0]\n",
    "        for i in range(art_mat.shape[0])\n",
    "    ]).reshape(-1,1)\n",
    "    return csr_matrix(scores)\n",
    "\n",
    "cos_train = cos_feature(train_exp)\n",
    "cos_val   = cos_feature(val_exp)\n",
    "cos_test  = cos_feature(test_exp)\n",
    "print('Cosine features computed.')"
]))

cells.append(code([
    "# ── 3.5 Lexical features ──\n",
    "def lexical_features(df_exp):\n",
    "    feats = []\n",
    "    for _, r in df_exp.iterrows():\n",
    "        art_toks = set(r['article'].split())\n",
    "        q_toks   = set(r['question'].split())\n",
    "        opt_toks = set(r['option'].split())\n",
    "        opt_list = r['option'].split()\n",
    "        art_str  = r['article']\n",
    "\n",
    "        option_length    = len(opt_list)\n",
    "        question_length  = len(r['question'].split())\n",
    "        keyword_overlap  = len(q_toks & opt_toks)\n",
    "        opt_in_article   = len(opt_toks & art_toks)\n",
    "        # position of first option token in article (normalised)\n",
    "        pos = 0.0\n",
    "        if opt_list and opt_list[0] in art_str:\n",
    "            idx = art_str.find(opt_list[0])\n",
    "            pos = idx / max(len(art_str), 1)\n",
    "        feats.append([option_length, question_length, keyword_overlap, opt_in_article, pos])\n",
    "    return csr_matrix(np.array(feats, dtype=np.float32))\n",
    "\n",
    "print('Computing lexical features (this may take a minute)...')\n",
    "lex_train = lexical_features(train_exp)\n",
    "lex_val   = lexical_features(val_exp)\n",
    "lex_test  = lexical_features(test_exp)\n",
    "print('Lexical features computed.')"
]))

